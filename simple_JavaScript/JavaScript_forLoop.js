//The syntax for a "for loop" is: for (variable = value; variable (condition < > != ==) value; change variable value)
for (i = 0; i <10; i++) {
  // we define i as the variable inside the for loop and set it equal to 0.
  // then "i<10" means For as long as i is less than ten, complete the execution sequence below before incrementing the number above.
  return i;
}

// This for loop sets i equal to 0, then says as long as i is less than 10, return the value of i. then increment i by one.

